-- ============================================================================
-- EXPORTAR DADOS - VERSÃO SIMPLES QUE FUNCIONA
-- Execute cada query separadamente e clique em "Download CSV"
-- ============================================================================

-- ============================================================================
-- 1. COLABORADORES (Todos os dados)
-- ============================================================================
SELECT * FROM colaboradores ORDER BY nome;

-- ============================================================================
-- 2. REGISTROS DE PONTO (Últimos 30 dias)
-- ============================================================================
SELECT * FROM registros_ponto 
WHERE data >= CURRENT_DATE - INTERVAL '30 days'
ORDER BY data DESC;

-- ============================================================================
-- 3. HOLERITES (Se existir)
-- ============================================================================
SELECT * FROM holerites;

-- ============================================================================
-- 4. FÉRIAS
-- ============================================================================
SELECT * FROM ferias 
ORDER BY data_inicio DESC;

-- ============================================================================
-- 5. SOLICITAÇÕES
-- ============================================================================
SELECT * FROM solicitacoes_funcionario 
ORDER BY data_solicitacao DESC;

-- ============================================================================
-- 6. CARGOS
-- ============================================================================
SELECT * FROM cargos ORDER BY nome;

-- ============================================================================
-- 7. DEPARTAMENTOS
-- ============================================================================
SELECT * FROM departamentos ORDER BY nome;

-- ============================================================================
-- 8. JORNADAS DE TRABALHO
-- ============================================================================
SELECT * FROM jornadas_trabalho ORDER BY nome;

